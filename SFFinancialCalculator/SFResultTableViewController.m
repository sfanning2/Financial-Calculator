#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [_investments count] + 1; // + Options Pool, + View Plot, + Back Button
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    if (cell == nil)
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:CellIdentifier];
    
    // Configure the cell...
    
    if ([indexPath row] == [_investments count]) {
        // Fist cell after investments
        cell.textLabel.text = @"Back";
    }
    else {
        cell.textLabel.text = [NSString stringWithFormat:@"Round %u", [indexPath row] + 1];
    }
    
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if ([indexPath row] == [self tableView:tableView numberOfRowsInSection:0] - 1) {
        // Last cell
        [self dismissViewControllerAnimated:YES completion:nil];
    }
    else {
        selectedIndex = [indexPath row];
        [self performSegueWithIdentifier:@"DisplayRoundDetails" sender:self];
    }
}

- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([segue.identifier isEqualToString:@"DisplayRoundDetails"])
    {
        SFRoundDetailsViewController *dest = segue.destinationViewController;
        dest.investment = [_investments objectAtIndex:selectedIndex];
        dest.roundNumber = selectedIndex + 1;
        
    }
}

